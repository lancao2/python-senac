// Crie um programa que para receber um numero de 1 a 7 e retornar o dia da semana correspondente.

let numeroDaSemana = 10;

if (numeroDaSemana == 1) {
    console.log("Domingo");
} else if (numeroDaSemana == 2) {
    console.log("Segunda-feira");
} else if (numeroDaSemana == 3) {
    console.log("Terça-feira");
} else if (numeroDaSemana == 4) {
    console.log("Quarta-feira");
} else if (numeroDaSemana == 5) {
    console.log("Quinta-feira");
} else if (numeroDaSemana == 6) {
    console.log("Sexta-feira");
} else if (numeroDaSemana == 7) {
    console.log("Sábado");
} else {
    console.log("Número inválido. Informe um número de 1 a 7.");
}